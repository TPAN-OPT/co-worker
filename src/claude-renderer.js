import { renderOrganizationMarkdown } from './organization-renderer.js'
import { stageGates } from './stage-gates.js'

export function renderClaudeMarkdown(workflow) {
  const agentList = Object.keys(workflow.roles)
    .map((roleId) => `- \`${roleId}\`: \`.claude/agents/${roleId}.md\``)
    .join('\n')

  const stageList = workflow.stages
    .map(
      (stage, index) => `${index + 1}. \`${stage.id}\` owned by \`${stage.owner}\`; output: ${
        stage.output ? `\`${stage.output}\`` : 'none'
      }; gates: ${formatGateList(stageGates(stage))}`
    )
    .join('\n')

  const organizationSection = renderOrganizationMarkdown(workflow.organization)

  return `# ${workflow.name} Claude Code Harness

This file was generated by TPAN-OPT/CO-WORKER from workflow version \`${workflow.version}\`.

## Operating Rules

- Follow the workflow stages in order unless a human lead explicitly approves a deviation.
- Stop at every gate until command output, review evidence, or human approval is attached.
- Use the role-specific agent files under \`.claude/agents/\` when delegating work.
- Do not perform external writes, credential changes, paid actions, destructive actions, or releases without explicit human approval.
- Run \`node scripts/verify-workflow.mjs --run-dir .tpan-opt-co-worker/runs/local\` before release or handoff.

${organizationSection}
## Claude Agents

${agentList}

## Workflow Stages

${stageList}
`
}

export function renderClaudeAgentMarkdown(roleId, role, workflow) {
  const ownedStages = workflow.stages.filter((stage) => stage.owner === roleId)
  const stageSections = ownedStages
    .map(
      (stage) => `## Stage: ${stage.id}

- Output: ${stage.output ? `\`${stage.output}\`` : 'none'}
- Required work: ${formatInlineList(stage.required)}
- Gates: ${formatGateList(stageGates(stage))}`
    )
    .join('\n\n')

  return `---
name: ${roleId}
description: ${formatYamlString(role.description)}
---

# ${roleId}

Act as \`${roleId}\` for the \`${workflow.name}\` workflow.

## Skills

${formatBulletList(role.skills)}

## MCP Servers

${formatBulletList(role.mcpServers || [])}

## Permissions

${formatBulletList(role.permissions)}

## Responsibilities

- Produce the required artifacts for owned stages.
- Keep changes inside the granted permission boundary.
- Stop when a gate needs evidence from tests, review, or a human lead.
- Record decisions, verification output, and approval links in durable artifacts.

${stageSections || '## Owned Stages\n\nThis role does not own any workflow stages.'}
`
}

function formatBulletList(items) {
  if (items.length === 0) {
    return '- none'
  }

  return items.map((item) => `- \`${item}\``).join('\n')
}

function formatInlineList(items) {
  return items.length === 0 ? 'none' : items.map((item) => `\`${item}\``).join(', ')
}

function formatGateList(gates) {
  if (gates.length === 0) {
    return 'none'
  }

  return gates
    .map((gate) => {
      const command = gate.command ? `: \`${gate.command}\`` : ''
      return `\`${gate.id}\` (${gate.type})${command}`
    })
    .join(', ')
}

function formatYamlString(value) {
  return JSON.stringify(value)
}
