// Phase 3 — team/human distribution. The same compiled workflow definition that
// drives code agents (OPT mode) is also the standard process a human teammate
// follows. This renderer turns that definition into PLAYBOOK.md: a top-to-bottom,
// checkbox-driven execution guide. In team mode every teammate runs the WHOLE
// pipeline below on their own product or module; the console aggregates those
// runs. The artifact is emitted in both modes (it documents the process) but the
// framing leads with the team scenario.
import { stageGates } from './stage-gates.js'

export function renderPlaybookMarkdown(workflow) {
  const mode = workflow.mode || 'opt'
  const stageSections = workflow.stages
    .map((stage, index) => renderStageSection(stage, index, workflow))
    .join('\n\n')

  return `# ${workflow.name} — Team Playbook

Generated by TPAN-OPT/CO-WORKER from workflow \`${workflow.name}@${workflow.version}\` (mode: \`${mode}\`).

This is the standard delivery process. Each teammate runs the **whole** pipeline
below on the product or module they own — same stages, same gates, different work.
Work top to bottom; never pass a gate without the evidence it asks for.

## How to use this playbook

- Track one copy of this checklist per product or module you own.
- Each stage names its owner role, what to produce, and the tools to use.
- A \`- [ ]\` gate is a checkpoint: capture the evidence, then tick it.
- **Command gates** — run the command and keep the output as evidence.
- **Manual gates** — get the named approver to sign off; record who approved and when.
- The console (\`.tpan-opt-co-worker/console/index.html\`) shows every teammate's run side by side.

## Stages at a glance

${renderOverview(workflow)}

## Stage detail

${stageSections}

## Roles reference

${renderRolesReference(workflow)}
`
}

function renderOverview(workflow) {
  return workflow.stages
    .map((stage, index) => {
      const gateCount = stageGates(stage).length
      const gateNote = gateCount === 1 ? '1 gate' : `${gateCount} gates`
      return `${index + 1}. **${stage.id}** — owner \`${stage.owner}\` · ${gateNote}`
    })
    .join('\n')
}

function renderStageSection(stage, index, workflow) {
  const lines = [`### Stage ${index + 1} — ${stage.id}`, '']
  const meta = [`**Owner:** \`${stage.owner}\``]
  if (stage.output) {
    meta.push(`**Produces:** \`${stage.output}\``)
  }
  if (stage.dependsOn && stage.dependsOn.length > 0) {
    meta.push(`**Start after:** ${stage.dependsOn.map((id) => `\`${id}\``).join(', ')}`)
  }
  lines.push(meta.join('  ·  '))

  const tools = formatTools(stage)
  if (tools) {
    lines.push('', `Tools: ${tools}`)
  }

  if (stage.required && stage.required.length > 0) {
    lines.push('', 'Required inputs:')
    for (const item of stage.required) {
      lines.push(`- \`${item}\``)
    }
  }

  lines.push('', 'Steps:')
  const nodes = stage.nodes || []
  if (nodes.length === 0) {
    lines.push(`- [ ] Complete the ${stage.id} work and produce the output above.`)
  } else {
    for (const node of nodes) {
      lines.push(renderNodeStep(node))
    }
  }

  const gateLines = renderGateChecklist(stageGates(stage))
  if (gateLines.length > 0) {
    lines.push('', 'Before moving on, verify:')
    lines.push(...gateLines)
  }

  return lines.join('\n')
}

function renderNodeStep(node) {
  const parts = [`- [ ] **${node.id}**`]
  if (node.output) {
    parts.push(`→ produce \`${node.output}\``)
  }
  const tools = formatTools(node)
  if (tools) {
    parts.push(`(${tools})`)
  }
  return parts.join(' ')
}

function renderGateChecklist(gates) {
  return gates.map((gate) => {
    const scope = gate.nodeId ? ` [node \`${gate.nodeId}\`]` : ''
    if (gate.type === 'command') {
      const command = gate.command ? ` — run \`${gate.command}\`` : ''
      return `- [ ] \`${gate.id}\`${scope} (command)${command}`
    }
    const detail = gate.description ? ` — ${gate.description}` : ' — approver sign-off required'
    return `- [ ] \`${gate.id}\`${scope} (manual)${detail}`
  })
}

function renderRolesReference(workflow) {
  return Object.entries(workflow.roles)
    .map(([roleId, role]) => {
      const description = role.description ? ` — ${role.description}` : ''
      const skills = formatList(role.skills)
      return `- \`${roleId}\`${description}\n  - Skills: ${skills} · Permissions: ${formatList(role.permissions)}`
    })
    .join('\n')
}

// Builds a compact "skills `a` · MCP `x` · hooks `h`" string from any entity that
// may carry per-stage or per-node tooling. Returns '' when nothing is bound so
// callers can omit the line entirely.
function formatTools(entity) {
  const segments = []
  if (entity.skills && entity.skills.length > 0) {
    segments.push(`skills ${inlineCodes(entity.skills)}`)
  }
  if (entity.mcpServers && entity.mcpServers.length > 0) {
    segments.push(`MCP ${inlineCodes(entity.mcpServers)}`)
  }
  if (entity.hooks && entity.hooks.length > 0) {
    segments.push(`hooks ${inlineCodes(entity.hooks)}`)
  }
  return segments.join(' · ')
}

function formatList(items) {
  return items && items.length > 0 ? inlineCodes(items) : 'none'
}

function inlineCodes(items) {
  return items.map((item) => `\`${item}\``).join(', ')
}
